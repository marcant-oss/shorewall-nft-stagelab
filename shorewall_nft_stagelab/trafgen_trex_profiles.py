"""Parameterised TRex profile templates for DoS-like workloads.

Each builder returns a Python source string that, when executed by the
TRex daemon via STLProfile.load_py / ASTFProfile.load_py, registers a
profile with the right streams or flow definitions. The returned text is
valid Python — callers may validate with ``compile(source, '<profile>',
'exec')``."""

import textwrap


def build_syn_flood_profile(
    *,
    src_cidr: str,
    dst_ips: tuple[str, ...],
    dst_ports: tuple[int, ...],
    rate_pps: int,
) -> str:
    """STL profile. Random src-IP within src_cidr, random dst-port from
    dst_ports, TCP SYN only, rate_pps packets per second aggregate."""
    return textwrap.dedent(f"""\
        # Auto-generated by shorewall_nft_stagelab.trafgen_trex_profiles
        from trex_stl_lib.api import (
            STLProfile, STLStream, STLTXCont, STLPktBuilder,
            STLVmFlowVar, STLVmFixIpv4, STLVmWrFlowVar,
        )
        from scapy.all import Ether, IP, TCP


        class SynFloodProfile:
            def __init__(self):
                self.src_cidr = {src_cidr!r}
                self.dst_ips = {list(dst_ips)!r}
                self.dst_ports = {list(dst_ports)!r}
                self.rate_pps = {rate_pps}

            def _pkt(self):
                base = Ether()/IP(src="0.0.0.0", dst=self.dst_ips[0])/TCP(
                    flags="S", dport=self.dst_ports[0], sport=1024,
                )
                vm = [
                    STLVmFlowVar("src_ip", min_value=self.src_cidr.split("/")[0],
                                 max_value=self.src_cidr.split("/")[0], size=4, op="random"),
                    STLVmWrFlowVar(fv_name="src_ip", pkt_offset="IP.src"),
                    STLVmFixIpv4(offset="IP"),
                ]
                return STLPktBuilder(pkt=base, vm=vm)

            def get_streams(self, direction=0, **kw):
                return [STLStream(
                    packet=self._pkt(),
                    mode=STLTXCont(pps=self.rate_pps),
                )]


        def register():
            return SynFloodProfile()
    """)


